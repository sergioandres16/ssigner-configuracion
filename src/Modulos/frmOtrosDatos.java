                firmador.setC_motivo(frmFirmador.configuracion.getVg_motivo());
                firmador.setC_otros("");
                //***FIN FIRMA VISIBLE***////
                firmador.setTipoResumen(frmFirmador.configuracion.getVg_algoritmo());
                grabarPropiedad(Global.CConstantes.CERTMOTIVO, frmFirmador.configuracion.getVg_motivo());
                grabarPropiedad(Global.CConstantes.CERTLOCACION, frmFirmador.configuracion.getVg_localidad());

