                cambiarColor(btnFirmar, java.awt.Color.white, new Color(97, 120, 134));
                    
                frmFirmador.configuracion.setVg_motivo(txtmotivo.getText());
                frmFirmador.configuracion.setVg_localidad(txtlocalidad.getText());
                
